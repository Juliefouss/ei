    public function findBySearch(SearchUser $searchUser)
    {
        $qb = $this->createQueryBuilder('a')
            ->andwhere('a.name LIKE :keyword')
            ->orWhere('a.firstname LIKE :keyword')
            ->orWhere('a.specialization LIKE :keyword')
